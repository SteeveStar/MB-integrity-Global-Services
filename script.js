const languageButtons = document.querySelectorAll(".lang-btn");

let currentLanguage = "en";

const applyTranslations = (lang) => {
  const dictionary = translations[lang] || translations.en;
  currentLanguage = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    if (dictionary[key]) {
      element.setAttribute("placeholder", dictionary[key]);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", isActive);
  });
};

const getDefaultLanguage = () => {
  const stored = localStorage.getItem("mbigs_language");
  if (stored && translations[stored]) {
    return stored;
  }
  const browserLang = (navigator.language || "en").slice(0, 2).toLowerCase();
  return translations[browserLang] ? browserLang : "en";
};

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const lang = button.dataset.lang;
    localStorage.setItem("mbigs_language", lang);
    applyTranslations(lang);
  });
});

const translateText = (key) => {
  if (!key) return "";
  return translations[currentLanguage]?.[key] || translations.en[key] || "";
};

const reviewsList = document.getElementById("reviewsList");
const reviewForm = document.getElementById("reviewForm");
const reviewStatus = document.getElementById("reviewStatus");

const renderReviews = (items) => {
  if (!reviewsList || !Array.isArray(items)) return;
  reviewsList.innerHTML = "";
  items.forEach((review) => {
    const card = document.createElement("article");
    card.className = "review-card";
    const message = document.createElement("p");
    message.textContent = `"${review.message}"`;
    const name = document.createElement("p");
    name.className = "review-name";
    name.textContent = `- ${review.name}`;
    card.appendChild(message);
    card.appendChild(name);
    reviewsList.appendChild(card);
  });
};

const loadReviews = async () => {
  if (!reviewsList) return;
  try {
    const response = await fetch("/api/reviews");
    if (!response.ok) return;
    const data = await response.json();
    if (Array.isArray(data) && data.length > 0) {
      renderReviews(data);
    }
  } catch (error) {
    // Keep fallback reviews if API is unavailable.
  }
};

if (reviewForm) {
  reviewForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    const formData = new FormData(reviewForm);
    const payload = {
      name: formData.get("reviewer")?.trim(),
      message: formData.get("review")?.trim(),
    };

    if (!payload.name || !payload.message) {
      if (reviewStatus) {
        reviewStatus.textContent = translateText("reviewError");
      }
      return;
    }

    try {
      const response = await fetch("/api/reviews", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });
      if (!response.ok) throw new Error("Submit failed");
      reviewForm.reset();
      if (reviewStatus) {
        reviewStatus.textContent = translateText("reviewSuccess");
      }
    } catch (error) {
      if (reviewStatus) {
        reviewStatus.textContent = translateText("reviewError");
      }
    }
  });
}

const navToggle = document.querySelector(".nav-toggle");
const navLinks = document.getElementById("primary-navigation");

if (navToggle && navLinks) {
  navToggle.addEventListener("click", () => {
    const isOpen = document.body.classList.toggle("nav-open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      document.body.classList.remove("nav-open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });
}

applyTranslations(getDefaultLanguage());
loadReviews();
